class Employee:
    name = "Varun"
    language = "Python"
    salary = 500000.0

Varun = Employee()
print(Varun.name, Varun.language, Varun.salary)


#Class is a blueprint of object
#Object is an instance of a class
#here, Employee is a class while Varun is an  object
#name, language, salary are class attributes